        <u>Tournament Rules:</u>
        <br></br>
        Bracket - The hosting team will host a SQUAD game session. Then they will invite his teammate, and their opponents to the game session and then begin their match.
        <br></br>
        <u>How To Win:</u>
        <br></br>
        For this tournament you will play two games. Each game you must keep track of your kills for each game played. The combined amount of kills for each game will determine which team will win the match. If out of the two games played the kills are tied a third map will be played to determine a winner for the match.
        <br></br>
        <u>Sabotage:</u>
        <br></br>
        You will party up as a SQUAD. Any player inside of that queued session MAY NOT destroy any bases made by their opponent, make them fall, or any other actions that may help lead to that person's death.
        <br>
        Examples of sabotage include but are not limited to:
        <br>
        
        <ol>
            <li>Breaking or weakening opponent's built or non built structures or cover by any means.</li><br>
            <li>Causing opponents to miss shots or miss opportunities for advancing their scores in any way shape or form.</li><br>
            <li>Building in front of your opponent.</li><br>
            <li>Boogie Bombing your opponent.</li><br>
            <li>The use of a boogie bomb on your opponent at any point in the game are subject to disqualifications.</li> <br>
        </ol>
        
